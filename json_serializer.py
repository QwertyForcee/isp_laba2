class JsonSerializer(Serializer):
    encoder = JsonEncoder()
    decoder = JsonDecoder()

    def dump(self,obj,f):
       f.write(self.dumps(obj))

    def dumps(self,obj):
        data = self.to_valid_dict(obj)
        return self.encoder.encode(data)

    def load(self,f):
        data = self.decoder.decode(f.read())
        obj = self.to_valid_obj(data)
        return obj

    def loads(self,data):
        obj = self.to_valid_obj(self.decoder.decode(data))
        return obj
